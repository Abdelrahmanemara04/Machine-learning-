WINDOW = 0.1

def aggregate_data(all_data_df, sensor_number):

    rows = all_data_df[['timestamp', 'value', 'label']].values

    aggr_rows = []

    bucket_start_ts   = rows[0, 0]
    bucket_label      = int(rows[0, 2])
    bucket_values     = [rows[0, 1]]

    def flush_bucket(ts, vals, lbl):
        aggr_rows.append({
            'ts':            ts,
            'average_value': float(np.mean(vals)),
            'min_value':     float(np.min(vals)),
            'max_value':     float(np.max(vals)),
            'label':         lbl,
        })

    for i in range(1, len(rows)):
        ts    = rows[i, 0]
        val   = rows[i, 1]
        label = int(rows[i, 2])

        if label != bucket_label or (ts - bucket_start_ts) > WINDOW:
            flush_bucket(bucket_start_ts, bucket_values, bucket_label)

            bucket_start_ts = ts
            bucket_label    = label
            bucket_values   = [val]
        else:
            bucket_values.append(val)

    flush_bucket(bucket_start_ts, bucket_values, bucket_label)

    aggr_df = pd.DataFrame(aggr_rows,
                           columns=['ts','average_value','min_value','max_value','label'])

    out_file = f'AGGR_DATA_{sensor_number}.csv'
    aggr_df.to_csv(out_file, index=False)
    print(f'Saved {len(aggr_df):,} aggregated rows → {out_file}')
    return aggr_df


