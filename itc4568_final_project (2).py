"""## Part 1C – Merge Sensor 1 & Sensor 2"""

def merge_aggr(aggr1_df, aggr2_df):

    a1 = aggr1_df.copy().reset_index(drop=True)
    a2 = aggr2_df.copy().reset_index(drop=True)

    merged_rows = []


    a2_ts   = a2['ts'].values
    a2_end  = a2_ts + WINDOW

    def find_overlapping_s2(ts):

        row_end = ts + WINDOW

        mask = (a2_ts < row_end) & (a2_end > ts)
        return np.where(mask)[0]

    used_s2 = set()


    for _, r1 in a1.iterrows():
        ts    = r1['ts']
        s2_idx = find_overlapping_s2(ts)

        if len(s2_idx) == 0:

            merged_rows.append({
                'ts':     ts,
                's1_avg': r1['average_value'],
                's1_min': r1['min_value'],
                's1_max': r1['max_value'],
                's2_avg': np.nan,
                's2_min': np.nan,
                's2_max': np.nan,
                'label':  r1['label'],
            })
        else:

            s2_sub = a2.iloc[s2_idx]
            merged_rows.append({
                'ts':     ts,
                's1_avg': r1['average_value'],
                's1_min': r1['min_value'],
                's1_max': r1['max_value'],
                's2_avg': s2_sub['average_value'].mean(),
                's2_min': s2_sub['min_value'].min(),
                's2_max': s2_sub['max_value'].max(),
                'label':  r1['label'],
            })
            used_s2.update(s2_idx.tolist())

    for i, r2 in a2.iterrows():
        if i in used_s2:
            continue
        merged_rows.append({
            'ts':     r2['ts'],
            's1_avg': np.nan,
            's1_min': np.nan,
            's1_max': np.nan,
            's2_avg': r2['average_value'],
            's2_min': r2['min_value'],
            's2_max': r2['max_value'],
            'label':  r2['label'],
        })

    merged_df = pd.DataFrame(merged_rows,
        columns=['ts','s1_avg','s1_min','s1_max','s2_avg','s2_min','s2_max','label'])
    merged_df.sort_values('ts', inplace=True)
    merged_df.reset_index(drop=True, inplace=True)

    merged_df.to_csv('AGGR_DATA_ALL.csv', index=False)
    print(f'Saved {len(merged_df):,} merged rows → AGGR_DATA_ALL.csv')
    return merged_df


